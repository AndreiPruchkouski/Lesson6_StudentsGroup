package by.htp.stringarray;

import java.util.Date;

public class Student {

	String name;
	String surname;
	Date birthDate;
	
	public Student() {
		
	}
	
	public Student(String name, String surname, Date birthDate) {
		this.name = name;
		this.surname = surname;
		this.birthDate = birthDate;
	}
	
}
